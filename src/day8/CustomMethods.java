public class CustomMethods {
	public static void main(String[] args) {
		sayAloha();
		System.out.println("------");
		
		sayAloha();
		System.out.println("------");
		
		sayAloha();
	}
	
	public static void sayAloha() {
		System.out.println("Aloha Friends!");
		System.out.println("Aloha Friends!");
		System.out.println("Aloha Friends!");
	}
	
	
}
