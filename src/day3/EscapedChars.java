package day3;

public class EscapedChars {
	public static void main(String[] args) {
		System.out.println("I said \"Hello\"");
		
		// \t - Tab space, \n - new line
		System.out.println("John\tDoe");
		System.out.println("John\nDoe");
		
		System.out.println("A\nB\nC");
		System.out.println("A\tB\tC"); 
	}
}
