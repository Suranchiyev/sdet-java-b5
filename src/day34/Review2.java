public class Review2 {
	public static void main(String[] args) {
		// What are the wrapper classes?
		// Wrapper classes are object versions of primitives.
		
		int num = 17;
		Integer numW = 17;
		
		char ch = 'a';
		Character chW = 'a';
		
		// byte    -> Byte
		// short   -> Short
		// int     -> Integer
		// long    -> Long
		// float   -> Float
		// double  -> Double
		// char    -> Character
		// boolean -> Boolean
	}
}
